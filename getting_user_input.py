# Key point input(), int(), float(), eval().
a = input("Please Enter the any value:-")  # It is considered as a sting value.
b = input("Please Enter the any value:-")
print(a+b)

# int
a = int(input("Please Enter the any value:-"))        # type-cast.
b = int(input("Please Enter the any value:-"))
print(a+b)

# float
c = float(input("Please Enter only float value:-"))
d = float(input("Please Enter only float value:-"))
print(c+d)

# eval
c = eval(input("Please Enter any type of value:-"))  # It is take bin, int, float, bit.
d = eval(input("Please Enter any type of value:-"))
print(c+d)
