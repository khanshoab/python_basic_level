# using in operator
a = 'hello'
print('h' in a)  # It is gives boolean value
print('H' in a)  # Membership operator is case-sensitive

name = ['hello', 'bay', 68, 'what', 'who', 'whom', 30, 40, 90, 'khan']
print(90 in name)

# using not in operator

a = 'hello'
print('h' not in a)  # It is gives boolean value
print('H' not in a)  # Membership operator is case-sensitive

name = ['hello', 'bay', 68, 'what', 'who', 'whom', 30, 40, 90, 'khan']
print(90 not in name)
