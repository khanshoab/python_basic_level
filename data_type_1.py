#  Mutable data types: (In this data type we can perform changes)
# list , dictionary , byte array.

# Immutable data types: (In this data type we can not perform changes)
# int , float , complex , string , tuple , set.

# int
a = 10
print(a, type(a))

# float
b = 20.44
print(b, type(b))

# complex
c = 2 + 2j
print(type(c))

# STRING DATA TYPE
s = 'khan@7860'
print(s, type(s))

s = "hello@786"
print(s, type(s))

s = ''' 
    hello
    my name 
    khan bh-au
'''
print(s, type(s))

s = '10'
print(s, type(s))

# LIST DATA TYPE [] IT IS MUTABLE.

hello = [3, 5, 'khan', 'hello', 90, 78.75]
print(hello, type(hello))

# TUPLE DATA TYPE () IT IS IMMUTABLE.

top = (10, 20, 'khan', "hello_moto", 78.67)
print(top, type(top))

# DICTIONARY DATA TYPE {} IT IS MUTABLE DATA TYPE

di = {1: 'hello', 2: 'myname', 3: 'khan', 4: 'apple'}
print(di, type(di))
print()
