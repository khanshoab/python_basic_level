# String it is the sequence of the characters.
# String indexing start at 0 (left --> wright).
# String indexing start at -1 (wright ---> left).


# String indexing
str1 = "My name is king khan I am from india"
print(str1[5])
print(str1[-1])
print(str1[-3])
print(str1[4])

# String slicing
print(str1[0:7])
print(str1[0:10:2])
print(str1[0::2])
# Reverse
print(str1[-1:-10:-2])
print(str1[-1::-1])